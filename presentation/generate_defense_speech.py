"""
generate_defense_speech.py
Генерирует Defense_Speech.docx — речь для защиты тестового задания.
Речь идёт последовательно по вкладкам презентации (сайта).
Архитектура — отдельным разделом в конце.

Запуск:  python generate_defense_speech.py
"""
from __future__ import annotations
from pathlib import Path

from docx import Document
from docx.shared import Pt, RGBColor, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

# ─── Цвета ───────────────────────────────────────────────────────────────────
GREEN   = RGBColor(0x10, 0xB9, 0x81)   # emerald
BLUE    = RGBColor(0x0E, 0xA5, 0xE9)   # sky
PURPLE  = RGBColor(0x8B, 0x5C, 0xF6)   # violet
AMBER   = RGBColor(0xF5, 0x9E, 0x0B)   # amber
RED     = RGBColor(0xEF, 0x44, 0x44)   # red
MUTED   = RGBColor(0x47, 0x55, 0x69)   # slate-600
DARK    = RGBColor(0x1E, 0x29, 0x3B)   # slate-800

# ─── Хелперы ─────────────────────────────────────────────────────────────────
def set_font(run, size=11, bold=False, italic=False, color=None):
    run.font.size = Pt(size)
    run.font.bold = bold
    run.font.italic = italic
    run.font.name = 'Calibri'
    if color:
        run.font.color.rgb = color

def add_heading(doc, text, level=1, color=GREEN):
    p = doc.add_heading(text, level=level)
    for run in p.runs:
        run.font.color.rgb = color
        run.font.name = 'Calibri'
    p.paragraph_format.space_before = Pt(14 if level == 1 else 8)
    p.paragraph_format.space_after  = Pt(4)
    return p

def add_para(doc, text, space_after=8, indent=0, bold=False, italic=False, color=DARK):
    p = doc.add_paragraph()
    run = p.add_run(text)
    set_font(run, 11, bold, italic, color)
    p.paragraph_format.left_indent  = Cm(indent)
    p.paragraph_format.space_after  = Pt(space_after)
    return p

def add_hint(doc, label, text):
    """Блок-подсказка для комиссии."""
    p = doc.add_paragraph()
    p.paragraph_format.left_indent  = Cm(0.8)
    p.paragraph_format.space_before = Pt(2)
    p.paragraph_format.space_after  = Pt(10)
    r1 = p.add_run(f"[Комиссии — {label}: ")
    set_font(r1, 9.5, bold=True, color=MUTED)
    r2 = p.add_run(text + "]")
    set_font(r2, 9.5, italic=True, color=MUTED)
    return p

def add_bullet(doc, text, bold_prefix=None, indent=0.5):
    p = doc.add_paragraph(style='List Bullet')
    if bold_prefix:
        rb = p.add_run(bold_prefix + ": ")
        set_font(rb, 11, bold=True)
    rt = p.add_run(text)
    set_font(rt, 11)
    p.paragraph_format.left_indent  = Cm(indent)
    p.paragraph_format.space_after  = Pt(4)
    return p

def add_hr(doc):
    p = doc.add_paragraph()
    pPr = p._p.get_or_add_pPr()
    pBdr = OxmlElement('w:pBdr')
    bottom = OxmlElement('w:bottom')
    bottom.set(qn('w:val'), 'single')
    bottom.set(qn('w:sz'), '6')
    bottom.set(qn('w:space'), '1')
    bottom.set(qn('w:color'), '334155')
    pBdr.append(bottom)
    pPr.append(pBdr)
    p.paragraph_format.space_before = Pt(4)
    p.paragraph_format.space_after  = Pt(8)

def add_tab_banner(doc, tab_num, tab_name, color=GREEN):
    p = doc.add_paragraph()
    run = p.add_run(f"  ВКЛАДКА {tab_num}: {tab_name.upper()}  ")
    run.font.size  = Pt(10)
    run.font.bold  = True
    run.font.name  = 'Calibri'
    run.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
    # Нет простого способа задать bgColor через python-docx без XML;
    # Вместо этого — просто подчёркивание цветом
    run.font.color.rgb = color
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after  = Pt(4)
    return p

