-- Q10: СЫРЫЕ ДАННЫЕ — ВСЕ 3161 СТРОКА
-- Фильтрация, сортировка прямо в DB Browser
-- ========================================================
SELECT
    source_label        AS площадка,
    entity_name         AS заказчик,
    procedure_number    AS процедура,
    lot_number          AS лот,
    SUBSTR(subject, 1, 80) AS предмет,
    ROUND(price_rub / 1e6, 2) AS цена_млн,
    published_at        AS опубликовано,
    method_name         AS способ_закупки,
    status              AS статус
FROM lots
ORDER BY price_rub DESC NULLS LAST;


-- ========================================================
-- Q11: БЮДЖЕТ ПО ГОДАМ
-- Сравнение 2024 vs 2025
-- ========================================================
SELECT
    year                                    AS год,
    COUNT(*)                                AS лотов_всего,
    SUM(CASE WHEN price_rub IS NOT NULL THEN 1 ELSE 0 END) AS с_ценой,
    ROUND(SUM(price_rub) / 1e9, 2)         AS бюджет_млрд,
    ROUND(AVG(price_rub) / 1e6, 2)         AS средний_лот_млн
FROM lots
GROUP BY year
ORDER BY year;


-- ========================================================
-- Q12: ДЕКАБРЬСКИЙ ПИК — АНОМАЛИЯ "ОСВОЕНИЯ БЮДЖЕТА"
-- Декабрь 2024 = 10x медианного месяца
-- ========================================================
SELECT
    year_month      AS месяц,
    COUNT(*)        AS лотов,
    ROUND(SUM(price_rub) / 1e9, 3) AS бюджет_млрд,
    ROUND(SUM(price_rub) * 100.0 / (SELECT SUM(price_rub) FROM lots WHERE price_rub IS NOT NULL AND year = 2024), 1) AS доля_в_2024_pct
FROM lots
WHERE year = 2024 AND price_rub IS NOT NULL
GROUP BY year_month
ORDER BY year_month;


-- ========================================================
-- Q13: МЕТОДЫ ЗАКУПОК — СТРУКТУРА
-- Открытые vs закрытые vs единственный поставщик
-- ========================================================
SELECT
    COALESCE(method_name, 'Не указано')     AS способ_закупки,
    source_label                            AS площадка,
    COUNT(*)                                AS лотов,
    ROUND(SUM(price_rub) / 1e9, 3)         AS бюджет_млрд
FROM lots
GROUP BY method_name, source_label
ORDER BY бюджет_млрд DESC NULLS LAST
LIMIT 20;


-- ========================================================
-- Q14: ПОИСК КОНКРЕТНОГО ЗАКАЗЧИКА (замени имя)
-- ========================================================
SELECT
    procedure_number, lot_number,
    SUBSTR(subject, 1, 100) AS предмет,
    ROUND(price_rub / 1e6, 2) AS цена_млн,
    published_at, method_name
FROM lots
WHERE entity_name LIKE '%Cloud%'     -- замени на нужное юрлицо
  AND price_rub IS NOT NULL
ORDER BY price_rub DESC;


